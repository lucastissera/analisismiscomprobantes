def normalize_lang(code: str | None) -> str:
    if not code:
        return DEFAULT_LANG
    c = str(code).lower().strip().split("-")[0]
    if c == "pt":
        c = "port"
    return c if c in SUPPORTED_LANGS else DEFAULT_LANG


def _t(lang: str, key: str, **kwargs: object) -> str:
    lang = normalize_lang(lang)
    table = TRANSLATIONS.get(lang) or TRANSLATIONS[DEFAULT_LANG]
    s = table.get(key)
    if s is None:
        s = TRANSLATIONS[DEFAULT_LANG].get(key, key)
    if kwargs:
        return s.format(**kwargs)
    return s


def tr_js_bundle(lang: str) -> dict[str, str]:
    """Textos para scripts (tema, arrastre de archivos)."""
    lg = normalize_lang(lang)
    keys = (
        "theme_dark",
        "theme_light",
        "aria_theme_dark",
        "aria_theme_light",
        "file_prefix",
        "err_invalid_ext",
    )
    return {k: _t(lg, k) for k in keys}


